    public static ArrayList<Booking> reservations = new ArrayList(); 
    
    // maximum capacity of the venue per time slot
    final int maximumCapacity = 50; 
    
      
    /**
     *  for each reservation the method checks if the same date and time slot
        have been booked and then 
        checks if with the new party size we exceed maximum capacity
     * @author Stavroula Niarchou
     * @param date
     * @param time
     * @param partySize
     * @return Boolean availability
     */
    public boolean checkAvailability(Date date, String time, int partySize){   
        int availabilityCount = 0;
        boolean availability = false;
        /*for each booking if the date and time match the requested date/time,
        the number of guests of that booking is added to the availability count
        */
        for (int i=0; i < reservations.size(); i++){
            if (date.equals(reservations.get(i).getDate())
                && time.equals(reservations.get(i).getTime()))               
            {
                availabilityCount += reservations.get(i).getPartySize();
            }
        }
        
        /* check if we exceed maximum capacity if we add the number 
        of the new guests to those that have already booked
        */
        if (partySize + availabilityCount <= maximumCapacity){
            availability = true;
            return availability;
        }
        return availability;
        
    }
    // end of checkAvailability method
    
    
    /**
     * creates new instance of class Booking and adds it to the array list 
       of reservations
     * @author Stavroula Niarchou 
     * @param bookingNumber
     * @param name
     * @param phoneNumber
     * @param date
     * @param time
     * @param partySize 
     */
    public void createNewBooking(
            String bookingNumber, 
            String name,
            String phoneNumber, 
            Date date, 
            String time,
            int partySize
    )
    {
        Booking myBooking = new Booking();
        myBooking.setBookingNumber(bookingNumber);
        myBooking.setName(name);
        myBooking.setPhoneNumber(phoneNumber);
        myBooking.setDate(date);
        myBooking.setTime(time);
        myBooking.setPartySize(partySize);
        reservations.add(myBooking);
    }
    // end of createNewBooking method
    
    
    /**
     * tries to parse the string phone number as long to check if it is numeric
     * @author Stavroula Niarchou 
     * @param myNumber
     * @return Boolean validNumber
     */
    public boolean isNumeric(String myNumber){
        boolean validNumber = false;
        try{
            long phoneNumber = Long.parseLong(myNumber);
            validNumber = true;
            return validNumber;
        }
        catch(Exception e){     
        }
        return validNumber;
    }
    // end of isNumeric method
    
    /**
     * receives the full date and sets hours, minutes and seconds to 0
     * @author Stavroula Niarchou
     * @param date
     * @return date (with time set to 0)
     */
    public static Date formatDate(Date date) {
        Calendar cal = Calendar.getInstance();
        cal.setTime(date);
        cal.set(Calendar.HOUR_OF_DAY, 0);
        cal.set(Calendar.MINUTE, 0);
        cal.set(Calendar.SECOND, 0);
        cal.set(Calendar.MILLISECOND, 0);
        return cal.getTime();
    }
