    /**
     * When the button is clicked it gets user input from the jFrame and 
     * calls the checkAvailability and createNewBooking methods
     * @author Stavroula Niarchou
     * @param evt 
     */
    private void btnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookActionPerformed
        // create a random booking number
        UUID BookingNumber = UUID.randomUUID();
        
       // get user input for date and set time element to 0
        Date myDate = jCalendar2.getDate();
        Date date = formatDate(myDate);
        
        // get user input for time, partySize, name , phone number
        String time;
        if (rdoBtnSlot1.isSelected()){
            time = "15:00-17:00";
        }
        else if (rdoBtnSlot2.isSelected()){
            time = "17:00-19:00";
        }
        else if (rdoBtnSlot3.isSelected()){
            time = "19:00-21:00";
        }
        else{
            time = "21:00-23:00";
        }
        
       int partySize = (Integer)SpinnerPartySize.getValue();
  
       String name = txtName.getText();
       
       String phoneNumber = txtPhoneNumber.getText();
       
       /*check if phone number is valid
       if it's not valid a message is displayed, 
       otherwise the program continues to execute
       */
       boolean valid = isNumeric(phoneNumber);
       if (valid == false){
          JOptionPane.showMessageDialog
            (null,
             "Please enter a valid phone number",
             "Invalid number", 
             JOptionPane.INFORMATION_MESSAGE); 
       }
       else{
        /* check availability and call the createNewBooking
           method if there is availability
        */
        boolean availability = checkAvailability(date, time, partySize);
        if (availability == true){
            createNewBooking(
                BookingNumber.toString(), 
                name, 
                phoneNumber, 
                date, 
                time, 
                partySize
            );
            JOptionPane.showMessageDialog(
               null, "Your booking reference is " + 
                BookingNumber.toString(), 
               "Booking Successful!", 
               JOptionPane.INFORMATION_MESSAGE
            );
        }
        else{
            JOptionPane.showMessageDialog(
                null, 
                """
                    We are sorry but we are fully booked on this date and time
                    Please choose a different date or time slot""", 
               "Booking unsuccessful", 
               JOptionPane.INFORMATION_MESSAGE);
        }
        
        txtName.setText("");
        txtPhoneNumber.setText("");
        rdoBtnSlot1.setSelected(true);
        SpinnerPartySize.setValue(1);
        
       }
      
    }//GEN-LAST:event_btnBookActionPerformed
